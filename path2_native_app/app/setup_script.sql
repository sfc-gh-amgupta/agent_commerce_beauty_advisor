        RETURN OBJECT_CONSTRUCT(
            ''success'', FALSE,
            ''error'', ''Cart session not found: '' || :session_id,
            ''message'', ''Could not find a cart session. Please create a cart first.''
        )::VARIANT;
    END IF;
    IF (LENGTH(:item_id) = 36
        AND SUBSTRING(:item_id, 9, 1) = ''-''
