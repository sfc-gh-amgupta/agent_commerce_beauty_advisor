        (SELECT session_id FROM CART_OLTP.CART_SESSIONS WHERE session_id = :session_id LIMIT 1),
        (SELECT session_id FROM CART_OLTP.CART_SESSIONS
          WHERE customer_id = :session_id AND status = ''active''
