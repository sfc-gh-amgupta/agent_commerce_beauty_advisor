CREATE OR REPLACE PROCEDURE app_schema.grant_callback(privileges ARRAY)
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS OWNER
AS $$
DECLARE
    pool_name VARCHAR;
    wh_name VARCHAR;
BEGIN
    pool_name := (SELECT CURRENT_DATABASE()) || '_POOL';
    wh_name := (SELECT CURRENT_DATABASE()) || '_WH';

    IF (ARRAY_CONTAINS('CREATE COMPUTE POOL'::VARIANT, :privileges)) THEN
        CREATE COMPUTE POOL IF NOT EXISTS IDENTIFIER(:pool_name)
            MIN_NODES = 1
            MAX_NODES = 1
            INSTANCE_FAMILY = CPU_X64_S
            AUTO_RESUME = TRUE
            AUTO_SUSPEND_SECS = 300;
    END IF;

    IF (ARRAY_CONTAINS('CREATE WAREHOUSE'::VARIANT, :privileges)) THEN
        CREATE WAREHOUSE IF NOT EXISTS IDENTIFIER(:wh_name)
            WAREHOUSE_SIZE = 'X-SMALL'
            AUTO_SUSPEND = 60
            AUTO_RESUME = TRUE;

        -- Create Cortex Search Services (require warehouse)
        EXECUTE IMMEDIATE '
            CREATE OR REPLACE CORTEX SEARCH SERVICE PRODUCTS.PRODUCT_SEARCH_SERVICE
                ON CONTENT
                ATTRIBUTES CONTENT_TYPE, ID, PRODUCT_ID, TITLE, BRAND, CATEGORY, SUBCATEGORY, COLOR_NAME, FINISH, CURRENT_PRICE, IS_VEGAN, IS_CRUELTY_FREE, SOURCE_IMAGE_URL, SEVERITY
                WAREHOUSE = ' || :wh_name || '
                TARGET_LAG = ''1 hour''
                EMBEDDING_MODEL = ''snowflake-arctic-embed-m-v1.5''
                COMMENT = ''Unified search across products, labels, ingredients, and warnings''
            AS (
                SELECT content_type, id, product_id, title, content, brand, category, subcategory, color_name, finish, current_price, is_vegan, is_cruelty_free, source_image_url, severity
                FROM PRODUCTS.PRODUCT_SEARCH_CONTENT
            )
        ';

        EXECUTE IMMEDIATE '
            CREATE OR REPLACE CORTEX SEARCH SERVICE PRODUCTS.LABEL_SEARCH_SERVICE
                ON CONTENT
                ATTRIBUTES CONTENT_TYPE, ID, PRODUCT_ID, PRODUCT_NAME, TITLE, BRAND, CATEGORY, SUBCATEGORY, IS_ALLERGEN, ALLERGEN_TYPE, CURRENT_PRICE, IS_VEGAN, IS_CRUELTY_FREE, LABEL_IMAGE_URL, SEVERITY
                WAREHOUSE = ' || :wh_name || '
                TARGET_LAG = ''1 hour''
                EMBEDDING_MODEL = ''snowflake-arctic-embed-m-v1.5''
                COMMENT = ''Focused search for product label content''
            AS (
                SELECT content_type, id, product_id, product_name, title, content, brand, category, subcategory, is_allergen, allergen_type, current_price, is_vegan, is_cruelty_free, label_image_url, severity
                FROM PRODUCTS.LABEL_SEARCH_CONTENT
            )
        ';

        EXECUTE IMMEDIATE '
            CREATE OR REPLACE CORTEX SEARCH SERVICE SOCIAL.SOCIAL_SEARCH_SERVICE
                ON CONTENT
                ATTRIBUTES CONTENT_TYPE, ID, PRODUCT_ID, TITLE, URL, AUTHOR_HANDLE, AUTHOR_NAME, PLATFORM, RATING, ENGAGEMENT_SCORE, VERIFIED_PURCHASE, SKIN_TONE, SKIN_TYPE, UNDERTONE, SENTIMENT_LABEL, IS_SPONSORED, POSTED_AT
                WAREHOUSE = ' || :wh_name || '
                TARGET_LAG = ''1 hour''
                EMBEDDING_MODEL = ''snowflake-arctic-embed-m-v1.5''
                COMMENT = ''Unified search across reviews, social mentions, and influencer content''
            AS (
                SELECT content_type, id, product_id, title, content, url, author_handle, author_name, platform, rating, engagement_score, verified_purchase, skin_tone, skin_type, undertone, sentiment_label, is_sponsored, posted_at
                FROM SOCIAL.SOCIAL_SEARCH_CONTENT
            )
        ';

        -- Load data using the warehouse
        CALL app_schema.load_data();
    END IF;

    IF (ARRAY_CONTAINS('BIND SERVICE ENDPOINT'::VARIANT, :privileges)) THEN
        CREATE SERVICE IF NOT EXISTS app_schema.agent_commerce_backend
            IN COMPUTE POOL IDENTIFIER(:pool_name)
            FROM SPECIFICATION_FILE = '/containers/service_spec.yaml'
            MIN_INSTANCES = 1
            MAX_INSTANCES = 1
            QUERY_WAREHOUSE = IDENTIFIER(:wh_name);

        GRANT USAGE ON SERVICE app_schema.agent_commerce_backend TO APPLICATION ROLE app_public;
    END IF;

    RETURN 'Privileges processed: ' || ARRAY_TO_STRING(:privileges, ', ');
END;
$$;

GRANT USAGE ON PROCEDURE app_schema.grant_callback(ARRAY) TO APPLICATION ROLE app_public;

