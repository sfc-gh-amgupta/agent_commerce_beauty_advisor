CREATE OR REPLACE HYBRID TABLE CART_OLTP.CART_SESSIONS (
    SESSION_ID VARCHAR(36) NOT NULL,
    CUSTOMER_ID VARCHAR(36),
    STATUS VARCHAR(30) NOT NULL DEFAULT 'active',
    SUBTOTAL_CENTS NUMBER(38,0) DEFAULT 0,
    TAX_CENTS NUMBER(38,0) DEFAULT 0,
    SHIPPING_CENTS NUMBER(38,0) DEFAULT 0,
    DISCOUNT_CENTS NUMBER(38,0) DEFAULT 0,
    TOTAL_CENTS NUMBER(38,0) DEFAULT 0,
    CURRENCY VARCHAR(3) DEFAULT 'USD',
    APPLIED_PROMO_CODES VARCHAR(1000),
    APPLIED_LOYALTY_POINTS NUMBER(38,0) DEFAULT 0,
    FULFILLMENT_TYPE VARCHAR(20),
    SHIPPING_ADDRESS VARCHAR(2000),
    BILLING_ADDRESS VARCHAR(2000),
    SHIPPING_METHOD_ID VARCHAR(36),
    IS_GIFT BOOLEAN DEFAULT FALSE,
    GIFT_MESSAGE VARCHAR(500),
    GIFT_RECIPIENT_EMAIL VARCHAR(255),
    IS_VALID BOOLEAN DEFAULT TRUE,
    VALIDATION_MESSAGE VARCHAR(500),
    IDEMPOTENCY_KEY VARCHAR(100),
    CREATED_AT TIMESTAMP_NTZ(9) DEFAULT CURRENT_TIMESTAMP(),
    UPDATED_AT TIMESTAMP_NTZ(9),
    EXPIRES_AT TIMESTAMP_NTZ(9),
    COMPLETED_AT TIMESTAMP_NTZ(9),
    PRIMARY KEY (SESSION_ID),
    INDEX IDX_CART_CUSTOMER(CUSTOMER_ID),
    INDEX IDX_CART_STATUS(STATUS)
);

CREATE OR REPLACE HYBRID TABLE CART_OLTP.CART_ITEMS (
    ITEM_ID VARCHAR(36) NOT NULL,
    SESSION_ID VARCHAR(36) NOT NULL,
    PRODUCT_ID VARCHAR(36) NOT NULL,
    VARIANT_ID VARCHAR(36),
    QUANTITY NUMBER(38,0) NOT NULL DEFAULT 1,
    UNIT_PRICE_CENTS NUMBER(38,0) NOT NULL,
    SUBTOTAL_CENTS NUMBER(38,0) NOT NULL,
    PRODUCT_NAME VARCHAR(255),
    VARIANT_NAME VARCHAR(100),
    PRODUCT_IMAGE_URL VARCHAR(1000),
    ADDED_AT TIMESTAMP_NTZ(9) DEFAULT CURRENT_TIMESTAMP(),
    UPDATED_AT TIMESTAMP_NTZ(9),
    PRIMARY KEY (ITEM_ID),
    FOREIGN KEY (SESSION_ID) REFERENCES CART_OLTP.CART_SESSIONS(SESSION_ID),
    INDEX IDX_CART_ITEMS_SESSION(SESSION_ID)
);

